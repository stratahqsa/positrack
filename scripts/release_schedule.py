# Both cutoffs are recipe constants (~/Downloads/p1release.md) — NOT derived live,
# they mark the last two meetings this report has been generated for.
MEETING_CUTOFF_ISO = "2026-07-03T10:30:00.000Z"   # Jul 3, 4PM IST
JUN29_CUTOFF_ISO = yt.EFFORT_CUTOFF_DEFAULT        # Jun 29, 4PM IST — same constant
                                                    # the Effort Report already uses.

# Reuse the shared engine's pure helpers rather than re-deriving them (avoids two
# "done state" / man-day definitions drifting apart in the same repo).
MAN_DAY = yt.MAN_DAY
is_done_state = yt.is_done_state
iso_to_ms = yt.iso_to_ms

MEETING_CUTOFF_MS = iso_to_ms(MEETING_CUTOFF_ISO)
JUN29_CUTOFF_MS = iso_to_ms(JUN29_CUTOFF_ISO)

# A story in a "re-open"-ish state gets bug drill-down. Same regex ytcore.py's own
# `report(rtype="briefing")` uses to detect reopened states, for consistency.
_REOPEN_RE = re.compile(r"re.?open", re.I)

# The shared ISSUE_FIELDS default has NO `links(...)` clause at all, which would
# silently break two-pass matching if relied on — define our own fields strings
# that request exactly what this report needs, including inline links so matching
# never needs a per-issue N+1 fetch.
_CF_SHAPE = "value(name,fullName,minutes,text)"
EPIC_FIELDS = (
    "idReadable,summary,created,resolved,"
    "customFields(name," + _CF_SHAPE + "),"
    "links(direction,linkType(name),issues(idReadable))"
)
STORY_FIELDS = EPIC_FIELDS  # identical shape; stories need the same fields + links

# Bulk "issue ID: ..." queries are capped by URL length, not YouTrack itself — the
