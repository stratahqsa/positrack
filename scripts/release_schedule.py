"""
release_schedule.py — the PXB1 Phase 1 "Release Schedule Tracker" PRODUCER.

WHY A SCRIPT, NOT THE COWORK/BROWSER FLOW: this report was originally generated by
a Claude Cowork session driving an authenticated Chrome tab (JWT lifted from
localStorage) to hit the YouTrack REST API directly and download a static HTML
file — see the reference recipe this script ports, `~/Downloads/p1release.md`.
That works for an on-demand, human-triggered generation, but the dashboard needs
this report refreshed automatically 3x/day with no browser/LLM in the loop. This
script replicates the exact same business logic using the same YOUTRACK REST API,
but authenticates with a stored permanent token (like scripts/snapshot.py) instead
of a browser session — so it can run unattended in GitHub Actions.

It composes ONE report from the shared engine (core/ytcore.py):
  * epics    — all PXB1 Phase-1 epics (TaskType: EPIC), done and not-done alike.
  * stories  — all PXB1 Phase-1 stories (TaskType: Story), top-level fetch.
  * matching — two-pass story->epic assignment via "Subtask" links (Pass 1: direct
               INWARD link; Pass 2: transitive through an already-mapped parent
               story). Unmatched stories are surfaced as orphans, never dropped.
  * bugs     — for every RE-OPEN story, its open bugs via a "Subtask" then
               "Bugs Reported" OUTWARD link chain (bug drill-down).
  * html     — a standalone report, styled to match the original recipe's output
               byte-for-byte (verbatim CSS/JS lifted from a real generated report).

Output: a single self-contained HTML file (default path: dist/release-schedule.html)
that the calling GitHub Actions workflow publishes to a GitHub Release. The Next.js
UI fetches it server-side via a proxy API route — no Python on Vercel, no token in
the browser.

Run:
    set -a; . ~/.positrack-yt.env; set +a
    python3 scripts/release_schedule.py [--out dist/release-schedule.html]

On any YouTrack 401/403/429 or missing token, this STOPS and prints
"BLOCKED on <reason>" — it never fabricates report data. Exit codes mirror
scripts/snapshot.py's: 3 = missing token, 4 = YouTrack auth/rate-limit error,
5 = any other engine error.

This script runs in CI/local (Python 3.9+), NOT on Vercel, so it may use richer
Python than the stdlib-only engine — but here it stays stdlib-only anyway.
"""
