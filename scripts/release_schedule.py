# ---------------------------------------------------------------- CLI
def main(argv=None):
    ap = argparse.ArgumentParser(description="PXB1 Phase 1 Release Schedule Tracker producer")
    ap.add_argument("--project", default=PROJECT)
    ap.add_argument("--scope", default=SCOPE)
    ap.add_argument("--base", default="", help="YouTrack base URL override")
    ap.add_argument("--out", default="dist/release-schedule.html")
    a = ap.parse_args(argv)

    token = os.environ.get("YT_TOKEN", "").strip()
    if not token:
        print("BLOCKED on missing token: set $YT_TOKEN (e.g. `set -a; . ~/.positrack-yt.env; set +a`).",
              file=sys.stderr)
        return 3

    ctx = yt.Ctx(token, a.base or os.environ.get("YT_BASE") or yt.DEFAULT_BASE)
    try:
        yt.whoami(ctx)
        data = build_report_data(ctx)
    except yt.YTError as e:
        code = e.status_code
        if code in (401, 403, 429):
            print("BLOCKED on YouTrack %s: %s" % (code, e.friendly_message), file=sys.stderr)
            return 4
        print("BLOCKED on engine error: %s" % e.friendly_message, file=sys.stderr)
        return 5

    html_out = render_html(data)
    out_dir = os.path.dirname(a.out)
    if out_dir:
        os.makedirs(out_dir, exist_ok=True)
    with open(a.out, "w", encoding="utf-8") as f:
        f.write(html_out)

    t = data["totals"]
    print(
        "release-schedule: %d epics (%d done / %d pending), %d stories, "
        "%d milestones, %d orphan stories, %d open bugs -> %s"
        % (
            t["epics_total"], t["epics_done"], t["epics_pending"], t["stories_total"],
            data["milestone_count"], len(data["orphan_story_ids"]), t["open_bugs"], a.out,
        )
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
