_CHUNK = 80


# ---------------------------------------------------------------- pure field helpers
# Ports of the JS recipe's cf()/cfMin()/cfTs()/cfArr()/maxSprint() helpers. Where the
# shared engine already has an equivalent (estimates, done-state, iso parsing), we
# reuse it directly instead of re-deriving it (see MAN_DAY/is_done_state/iso_to_ms
# above) — these four are the genuinely new pieces this report needs.

def cf_str(issue, name):
    """String presentation of a named custom field (State/Assignee/etc.), via the
    engine's existing name->fullName->login->presentation->text fallback chain."""
    return yt.vname(yt._cf_named(issue, name))


def cf_minutes(issue, name):
    """Minutes from a period-type custom field (Estimation/Spent fields). Wraps
    yt._cf_minutes with a bare-number fallback (the JS recipe's cfMin() is more
    defensive than the engine's dict-only version) rather than re-deriving it."""
    v = yt._cf_named(issue, name)
    if isinstance(v, dict) and v.get("minutes") is not None:
        return int(v["minutes"])
    if isinstance(v, (int, float)):
        return int(v)
    return 0


def cf_timestamp(issue, name):
    """Epoch-ms from a bare-numeric DateIssueCustomField (Deadline Date/QA
    Deadline). No engine equivalent exists — these fields are new to this report."""
    v = yt._cf_named(issue, name)
    return int(v) if isinstance(v, (int, float)) else None


def cf_array(issue, name):
    """List of presentation strings from a multi-value custom field (Sprints)."""
    v = yt._cf_named(issue, name)
    if v is None:
        return []
    if isinstance(v, list):
        return [yt.vname(x) for x in v if yt.vname(x)]
    single = yt.vname(v)
    return [single] if single else []


_SPRINT_NUM_RE = re.compile(r"\d+")


def max_sprint(names):
    """The 'latest' sprint name, sorted by the numeric suffix in its name (e.g.
    beta1-20 > beta1-19), tie-broken alphabetically. None if the list is empty."""
    if not names:
        return None

    def key(nm):
        # The LAST numeric group is the sprint suffix (e.g. "beta1-19" -> 19, not
        # the "1" in "beta1") — a bare .search() would grab the first number.
        matches = _SPRINT_NUM_RE.findall(nm or "")
        return (int(matches[-1]) if matches else 0, nm or "")

    return sorted(set(names), key=key)[-1]


def is_reopen_state(state):
    return bool(_REOPEN_RE.search(state or ""))


# ---------------------------------------------------------------- issue normalization
def _normalize(issue):
    """Recipe-shaped issue -> the flat dict every downstream function operates on.
    Pure; no network. Shared by both epics and stories (same field shape)."""
    return {
        "id": issue.get("idReadable") or "",
        "summary": issue.get("summary") or "",
        "state": cf_str(issue, "State"),
        "assignee": cf_str(issue, "Assignee"),
        "created": issue.get("created"),
        "resolved": issue.get("resolved"),
        "dev_est": cf_minutes(issue, "Server Estimation"),
        "ui_est": cf_minutes(issue, "UI Estimation"),
        "qa_est": cf_minutes(issue, "Testing Estimation"),
        # ASSUMPTION (documented, not verified live — no YT_TOKEN in this dev
        # environment): the built-in "Spent time" period field. ytcore.py notes
        # this field can be inflated by workflow-propagated bug time at the EPIC
        # level; at story level it is what the source recipe's cfMin() reads
        # directly, so we match that rather than introduce a work-item sweep the
        # recipe never used. Verify against a live `describe --project PXB1` if
        # numbers look inflated once real data is available.
        "spent": cf_minutes(issue, "Spent time"),
        "dev_deadline": cf_timestamp(issue, "Deadline Date"),
        "qa_deadline": cf_timestamp(issue, "QA Deadline"),
        "sprints": cf_array(issue, "Sprints"),
        "links": issue.get("links") or [],
    }


# ---------------------------------------------------------------- two-pass matching
def build_story_epic_map(epics, stories):
    """PURE. {story_id: epic_id} via two passes over already-fetched, links-shaped
    dicts (no network — Pass-1/Pass-2 both read `links` inline from the fetch).

    Pass 1: a story with an INWARD "Subtask" link naming a known epic id maps
    directly. Pass 2: iterate to a fixed point over stories Pass-1 left unmapped —
    if a story's INWARD "Subtask" parent is itself a mapped STORY (not an epic),
    the child transitively inherits that story's epic. Anything left after Pass 2
    converges is an orphan — surfaced, never silently dropped.

    Precedence (explicit, since the source recipe's "two-pass" framing doesn't
    state a tie-break rule): Pass 1 always wins; Pass 2 only fills gaps Pass 1
    left. A story is never reassigned once Pass 1 has matched it.
    """
    epic_ids = {e["id"] for e in epics}
    story_ids = {s["id"] for s in stories}
    by_id = {s["id"]: s for s in stories}

    def subtask_parents(issue):
        parents = []
        for lk in issue.get("links") or []:
            if (lk.get("linkType") or {}).get("name") == "Subtask" and lk.get("direction") == "INWARD":
                for p in lk.get("issues") or []:
                    pid = p.get("idReadable")
                    if pid:
                        parents.append(pid)
        return parents

    mapping = {}
    unmapped = []
    for s in stories:
        parents = subtask_parents(s)
        direct_epic = next((p for p in parents if p in epic_ids), None)
        if direct_epic:
            mapping[s["id"]] = direct_epic
        else:
            unmapped.append(s["id"])

    # Pass 2: iterate to a fixed point — a grandparent-story chain is plausible.
    changed = True
    while changed and unmapped:
        changed = False
        still_unmapped = []
        for sid in unmapped:
            story = by_id.get(sid)
            parents = subtask_parents(story) if story else []
            parent_epic = next(
                (mapping[p] for p in parents if p in story_ids and p in mapping), None,
            )
            if parent_epic:
                mapping[sid] = parent_epic
                changed = True
            else:
                still_unmapped.append(sid)
        unmapped = still_unmapped

    orphan_story_ids = unmapped
    return mapping, orphan_story_ids


# ---------------------------------------------------------------- bug drilldown
def _children_by_parent(ctx, parent_ids, link_name, chunk=_CHUNK):
    """{parent_id: [child_id, ...]} for the OUTWARD `link_name` children of
    `parent_ids`, via a CHUNKED `issue ID: ...` bulk query (no N+1). Generalizes
    ytcore._child_parent_map's query shape but returns the inverted, list-valued
    mapping this report needs — reused for BOTH bug-drilldown hops by parameter."""
    out = {}
    ids = [i for i in parent_ids if i]
    for start in range(0, len(ids), chunk):
        batch = ids[start:start + chunk]
        q = "issue ID: " + ", ".join(batch)
        issues = yt.get_issues(
            ctx, q, fields="idReadable,links(direction,linkType(name),issues(idReadable))", top=300,
        )
        for it in issues:
            pid = it.get("idReadable")
            for lk in it.get("links") or []:
                if (lk.get("linkType") or {}).get("name") == link_name and lk.get("direction") == "OUTWARD":
                    for c in lk.get("issues") or []:
                        cid = c.get("idReadable")
                        if cid:
                            out.setdefault(pid, [])
                            if cid not in out[pid]:
                                out[pid].append(cid)
    return out


def bug_drilldown(ctx, reopen_story_ids):
    """{story_id: [{"id","summary","state"}, ...]} of OPEN bugs only, reached via
    RE-OPEN story -> "Subtask" OUTWARD (dev ticket) -> "Bugs Reported" OUTWARD
    (bug). Two chunked bulk-link lookups + one chunked detail fetch — no N+1
    regardless of how many RE-OPEN stories exist."""
    if not reopen_story_ids:
        return {}
    story_to_tickets = _children_by_parent(ctx, reopen_story_ids, "Subtask")
    all_tickets = sorted({t for tickets in story_to_tickets.values() for t in tickets})
    if not all_tickets:
        return {}
    ticket_to_bugs = _children_by_parent(ctx, all_tickets, "Bugs Reported")
    all_bug_ids = sorted({b for bugs in ticket_to_bugs.values() for b in bugs})
    if not all_bug_ids:
        return {}

    bug_details = {}
    for start in range(0, len(all_bug_ids), _CHUNK):
        batch = all_bug_ids[start:start + _CHUNK]
        q = "issue ID: " + ", ".join(batch)
        for it in yt.get_issues(ctx, q, fields="idReadable,summary,customFields(name,value(name))", top=300):
            bug_details[it.get("idReadable")] = {
                "id": it.get("idReadable"),
                "summary": it.get("summary") or "",
                "state": cf_str(it, "State"),
            }

    result = {}
    for sid, tickets in story_to_tickets.items():
        bugs = []
        for t in tickets:
            for bid in ticket_to_bugs.get(t, []):
                bug = bug_details.get(bid)
                if bug and not is_done_state(bug["state"]):  # "open" = not done/resolved
                    bugs.append(bug)
        if bugs:
            result[sid] = bugs
    return result


# ---------------------------------------------------------------- per-epic computation
def epic_state_badge(all_stories, epic_own_state):
    """(display_text, is_done) for an epic's state badge.
      * zero stories  -> the epic's own State field, not derived from children.
      * exactly 1 story -> that story's literal state text (not "DONE"/"NOT DONE").
      * all done      -> ("DONE", True)
      * any pending   -> ("NOT DONE", False)
    """
    if not all_stories:
        return epic_own_state or "—", is_done_state(epic_own_state)
    if len(all_stories) == 1:
        s = all_stories[0]
        return s["state"] or "—", is_done_state(s["state"])
    all_done = all(is_done_state(s["state"]) for s in all_stories)
    return ("DONE" if all_done else "NOT DONE"), all_done


def visible_stories(epic_done, all_stories):
    """Story Visibility Rules (verbatim from the recipe):
      NOT DONE epic -> pending stories + stories done after the MEETING cutoff.
      DONE epic     -> only stories resolved after the JUN29 cutoff.
    Cutoff boundary is EXCLUSIVE ("after X"), i.e. strictly greater-than."""
    if epic_done:
        return [
            s for s in all_stories
            if s["resolved"] is not None and s["resolved"] > JUN29_CUTOFF_MS
        ]
    out = []
    for s in all_stories:
        if not is_done_state(s["state"]):
            out.append(s)
        elif s["resolved"] is not None and s["resolved"] > MEETING_CUTOFF_MS:
            out.append(s)
    return out


def epic_rollup(epic_done, all_stories, visible):
    """Estimates/Spent/Sprint rollup at epic level (verbatim from the recipe):
      NOT DONE epics -> from PENDING stories only (a stricter subset than
                        "visible", which also includes done-after-meeting stories).
      DONE epics     -> from the visible (resolved-after-Jun29) stories."""
    pool = visible if epic_done else [s for s in all_stories if not is_done_state(s["state"])]
    sprints = max_sprint([sp for s in pool for sp in s["sprints"]])
    return {
        "dev": sum(s["dev_est"] for s in pool),
        "ui": sum(s["ui_est"] for s in pool),
        "qa": sum(s["qa_est"] for s in pool),
        "spent": sum(s["spent"] for s in pool),
        "sprint": sprints,
    }


def epic_resolved_date(epic_done, visible):
    """Max resolved date across visible stories — only meaningful when the epic is
    DONE (per the recipe: 'only shown when epicState === DONE')."""
    if not epic_done:
        return None
    dates = [s["resolved"] for s in visible if s["resolved"] is not None]
    return max(dates) if dates else None


# ---------------------------------------------------------------- milestone grouping
_DAY_MS = 86400000


def milestone_key(epic):
    """max(Dev Deadline, QA Deadline) for one epic, bucketed by CALENDAR DAY (not
    exact-ms equality) so same-day deadlines a few minutes apart still group
    together. None (both deadlines missing) -> the caller buckets these last,
    surfaced as their own group rather than silently dropped."""
    candidates = [d for d in (epic.get("dev_deadline"), epic.get("qa_deadline")) if d is not None]
    if not candidates:
        return None
    ts = max(candidates)
    return (ts // _DAY_MS) * _DAY_MS


def group_by_milestone(epics):
    """[{"date_ms": int|None, "epics": [...], "all_done": bool}, ...] sorted
    ascending, with the no-deadline bucket (date_ms=None) sorted LAST."""
    buckets = {}
    for e in epics:
        key = milestone_key(e)
        buckets.setdefault(key, []).append(e)

    def sort_key(k):
        return (k is None, k if k is not None else 0)

    groups = []
    for key in sorted(buckets, key=sort_key):
        group_epics = buckets[key]
        all_done = all(e["_epic_done"] for e in group_epics)
        groups.append({"date_ms": key, "epics": group_epics, "all_done": all_done})
    return groups


# ---------------------------------------------------------------- compose (network)
def _fetch_epics(ctx):
    q = "project: %s TaskType: EPIC Scope: {%s}" % (PROJECT, SCOPE)
    raw = yt.get_issues(ctx, q, fields=EPIC_FIELDS, top=300)
    exclude = set(EXCLUDE_EPIC_IDS)
    return [_normalize(e) for e in raw if e.get("idReadable") not in exclude]


def _fetch_stories(ctx):
    # Top-level fetch, per the recipe's explicit warning that stories nested under
    # an epic do not reliably return fully-resolved customFields.
    q = "project: %s Scope: {%s} TaskType: Story" % (PROJECT, SCOPE)
    raw = yt.get_issues(ctx, q, fields=STORY_FIELDS, top=300)
    return [_normalize(s) for s in raw]


def build_report_data(ctx):
    epics = _fetch_epics(ctx)
    stories = _fetch_stories(ctx)

    story_epic_map, orphan_story_ids = build_story_epic_map(epics, stories)
    stories_by_epic = {}
    for s in stories:
        eid = story_epic_map.get(s["id"])
        if eid:
            stories_by_epic.setdefault(eid, []).append(s)

    reopen_story_ids = [s["id"] for s in stories if is_reopen_state(s["state"])]
    bugs_by_story = bug_drilldown(ctx, reopen_story_ids)

    for e in epics:
        all_stories = stories_by_epic.get(e["id"], [])
        badge_text, epic_done = epic_state_badge(all_stories, e["state"])
        visible = visible_stories(epic_done, all_stories)
        e["_all_stories"] = all_stories
        e["_visible_stories"] = visible
        e["_epic_state_badge"] = badge_text
        e["_epic_done"] = epic_done
        e["_resolved_date"] = epic_resolved_date(epic_done, visible)
        e["_rollup"] = epic_rollup(epic_done, all_stories, visible)

    milestones = group_by_milestone(epics)

    total_pending_epics = sum(1 for e in epics if not e["_epic_done"])
    totals = {
        "dev": sum(e["_rollup"]["dev"] for e in epics),
        "ui": sum(e["_rollup"]["ui"] for e in epics),
        "qa": sum(e["_rollup"]["qa"] for e in epics),
        "spent": sum(e["_rollup"]["spent"] for e in epics),
        "epics_total": len(epics),
        "epics_done": len(epics) - total_pending_epics,
        "epics_pending": total_pending_epics,
        "stories_total": len(stories),
        "stories_done": sum(1 for s in stories if is_done_state(s["state"])),
        "stories_pending": sum(1 for s in stories if not is_done_state(s["state"])),
        "open_bugs": sum(len(v) for v in bugs_by_story.values()),
        "reopen_stories": sum(1 for sid in bugs_by_story if sid in reopen_story_ids),
    }
    totals["total_est"] = totals["dev"] + totals["ui"] + totals["qa"]
    totals["remaining"] = max(0, totals["total_est"] - totals["spent"])

    final_ts = max(
        (m["date_ms"] for m in milestones if m["date_ms"] is not None), default=None,
    )

    return {
        "generated_at_iso": datetime.datetime.now(datetime.timezone.utc).isoformat(),
        "project": PROJECT,
        "scope": SCOPE,
        "engine_version": ENGINE_VERSION,
        "milestones": milestones,
        "orphan_story_ids": orphan_story_ids,
        "bugs_by_story": bugs_by_story,
        "totals": totals,
        "final_ts": final_ts,
        "milestone_count": len([m for m in milestones if m["date_ms"] is not None]),
    }


