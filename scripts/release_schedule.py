def render_milestone_group(group, bugs_by_story, now_ms, index):
    date_ms = group["date_ms"]
    all_done = group["all_done"]
    color = _milestone_color(date_ms, now_ms, all_done)
    if date_ms is None:
        label = "\U0001f4c5 No deadline set"
    else:
        icon = "✅" if all_done else "\U0001f4c5"
        label = "%s %s" % (icon, _fmt_date(date_ms, "%a, %d %b %Y"))
    n_epics = len(group["epics"])
    n_stories = sum(len(e["_visible_stories"]) for e in group["epics"])
    group_id = "grp_%d" % index
    rows = "".join(render_epic_row(e, bugs_by_story) for e in group["epics"])
    return (
        '<div class="dg"><div class="dh" style="background:%s" onclick="ts(\'%s\')">%s '
        '<span class="cnt">%d epic%s</span><span class="cnt">%d stor%s</span></div>'
        '<div id="%s" style="overflow-x:auto;background:#fff;border:1px solid #e2e8f0;'
        'border-top:none;border-radius:0 0 6px 6px"><table style="width:100%%;'
        'border-collapse:collapse">%s%s</table></div></div>'
    ) % (
        color, group_id, label,
        n_epics, "" if n_epics == 1 else "s",
        n_stories, "y" if n_stories == 1 else "ies",
        group_id, _TABLE_HEAD, rows,
    )


